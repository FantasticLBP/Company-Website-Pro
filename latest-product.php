<script>
    var $page = 1;
    function updateUI($this,page) {
        $(".pagination li").each(function () {
            $(this).removeClass("active");
        });
        $($this).parent().addClass("active");
        $page = page;
        refreshView(page);
    }

    function refreshView(page) {
        var $node = $("#shopZone");
        $node.empty();
        //加载产品列表
        $.ajax({
            dataType: "json",
            type: "get",
            url: "controller/productlist.php?type=普通商品&page="+$page,
            success: function (data) {
                if (data.code == 200) {
                    var datas = data.data.datas;
                    if(datas.length <= 0){
                        $('<div class="row center-block" style="margin: 10px;" >喵~没找到相关的商品哦。</div>').appendTo($node);
                        return ;
                    }
                    for (var i = 0; i < datas.length; i++) {
                        console.log(datas[i].id);
                        $('<div class="col-md-3 col-sm-6 masonry-grid-item">' +
                            '<div class="listing-item">' +
                            '<div class="overlay-container">' +
                            '<img src="'+ datas[i].image1 + '" alt="">' +
                            '<a href="productDetail.php?id='+datas[i].id+'&type=普通商品" class="overlay small">' +
                            '<i class="fa fa-plus"></i>' +
                            '<span>查看详情</span>' +
                            '</a>' +
                            '</div>' +
                            '<div class="listing-item-body clearfix">' +
                            '<h3 class="title"><a href="productDetail.php?id='+datas[i].id+'&type=普通商品">' + datas[i].productName + '</a></h3>' +
                            '<p>' + datas[i].unit + '</p>' +
                            '<span class="price">¥' + datas[i].price + '</span>' +
                            '<div class="elements-list pull-right">' +
                            '<a href="productDetail.php?id='+datas[i].id+'&type=普通商品" class="wishlist" title="wishlist"><i class="fa fa-heart-o"></i></a>' +
                            '<a href="productDetail.php?id='+datas[i].id+'&type=普通商品"><i class="fa fa-shopping-cart pr-10"></i>加入购物车</a>' +
                            '</div>' +
                            '</div>' +
                            '</div>' +
                            '</div>').appendTo($node);
                    }
                    $node.css("height","auto");
                    if(page != "" && page != 0){
                        return ;
                    }
                    var $pagetarget = $(".pagination");
                    $pagetarget.empty();
                    var pages = Math.ceil(data.data.totals/28);
                    for(var $i=0;$i<pages+2;$i++){
                        if($i == 0){
                            $('<li><a>«</a></li>').appendTo($pagetarget);
                        }else if($i == 1){
                            $('<li class="active"><a onclick="updateUI(this,1);">1 <span class="sr-only">(current)</span></a></li>').appendTo($pagetarget);
                        }else if($i == pages+1){
                            $('<li><a>»</a></li>').appendTo($pagetarget);
                        }else{
                            $('<li><a onclick="updateUI(this,'+$i+');">'+$i+'</a></li>').appendTo($pagetarget);
                        }
                    }
                }
            },
            error: function (code, message, data) {
                alert("发生错误：" + message);
            }
        });
    }

